package JavaCode.random_records.N601_700;

import JavaCode.data_structure_binary_tree.TreeNode;

import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;
import java.util.Queue;

public class N662_maximum_width_of_binary_tree {
    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     *     int val;
     *     TreeNode left;
     *     TreeNode right;
     *     TreeNode(int x) { val = x; }
     * }
     */
    private int res;
    public int widthOfBinaryTree(TreeNode root) {
        res=0;
        dfs(root,1,1,new ArrayList<>());
        return res;
    }

    private void dfs(TreeNode root, int level, int idx, List<Integer> list) {
        if (root==null)return;
        if(level>list.size())list.add(idx);
        res=Math.max(res,idx-list.get(list.get(level-1)+1));
        dfs(root.left,level+1,idx*2,list);
        dfs(root.right,level+1,idx*2+1,list);
    }
}
